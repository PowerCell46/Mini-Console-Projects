function changeCardContent(index, view) {
    const card = document.querySelector(".card");
    card.classList.remove("fade-in-animation");

    document.querySelector("#current-card-index").textContent = index;
    document.querySelector(".images").src = cardsData[view][index].imageUrl;
    document.querySelector(".shown h5").textContent = cardsData[view][index].title;
    document.querySelector(".hidden p").textContent = cardsData[view][index].description;

    void card.offsetWidth;
    card.classList.add("fade-in-animation");
}


function incrementCard(e, view) {
    const currentIndex = Number(document.querySelector("#current-card-index").textContent) + 1 === cardsData[view].length ? 0 : Number(document.querySelector("#current-card-index").textContent) + 1;
    // console.log(currentIndex);

    changeCardContent(currentIndex, view);
}

function decrementCard(e, view) {
    const currentIndex = Number(document.querySelector("#current-card-index").textContent) - 1 < 0 ? cardsData[view].length - 1 : Number(document.querySelector("#current-card-index").textContent) - 1;
    // console.log(currentIndex);

    changeCardContent(currentIndex, view);
}
